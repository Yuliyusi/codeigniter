        Being overweight in later life puts you at higher risk of brain decline, 
        Korean research suggests.
        <br /><br />
        A study of 250 people aged between 60 and 70 found those with a high 
        body mass index (BMI) and big waists scored more poorly in cognitive tests.
        <br /><br />
        The Alzheimer's Society said the research, in the journal Age and Ageing,
        added to evidence that excess body fat can affect brain function.
        <br /><br />
        Lifestyle changes can help make a difference, it said.
        <br /><br />
        The study looked at the relationship between fat levels and cognitive 
        performance in adults aged 60 or over.
